class Switch13 
{
	public static void main(String[] args)
{
	int x = 0 ;
	int num = 1;
	while (x <= 10)
	{
	switch(num) {
	case 1:
		System.out.println("1");
		break;
	case 2:
		System.out.println("2");
		break;
	case 3:
		System.out.println("3");
		break;
	default:
		System.out.println("Invalid");

	}
	num++;
	x++;
}
}
}
